let numero = 1;

console.log("O valor atual da variável número é: ", numero);

// Pós-Incremento (soma/incrementa somente depois de exibir o valor)
console.log("O valor Pós Incremento da variável número é: ", numero++);

// console.log(`O valor Pós Incremento da variável número é: ${numero++}, o número atualizado: ${numero}`);

console.log("O valor Pré Incremento da variável número atualizado: ", numero);

// Atribuição com soma
console.log("Atribuição com soma (número = número + 2): ", numero += 2);